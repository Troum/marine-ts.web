<script setup lang="ts">
/**
 * Декоративный правый rail из макета Marin (Figma).
 *
 * Один и тот же SVG используется во всех тёмных секциях страницы «О нас»:
 * вертикальная и горизонтальная штриховые линии, серые «стрелки-указатели»
 * и центральный ромб — единый layout-маркер сетки.
 *
 * РАСТЯЖЕНИЕ БЕЗ ДЕФОРМАЦИИ.
 * Раньше viewBox был фиксирован `0 0 92 1000` + `preserveAspectRatio="none"`,
 * из-за чего ромб (квадрат, повёрнутый −45°) визуально становился вытянутым
 * по высоте секции. Теперь:
 *   • Реальная высота rail измеряется через `ResizeObserver` родителя.
 *   • viewBox задаётся `0 0 92 H` (где H — фактическая высота в пикселях),
 *     благодаря чему 1 единица SVG = 1 пиксель и геометрия не искажается.
 *   • Верхняя группа стрелок прижата к верху (фиксированные y от 0 до 284).
 *   • Нижняя группа стрелок + ромб + горизонталь прижаты к низу
 *     (через `transform="translate(0, H − 1000)"`).
 *   • Растягивается только центральная красная вертикаль — она и должна
 *     заполнять всё свободное место по дизайну.
 *
 * По умолчанию rail приглушённый (`opacity 0.45`), цвета штрихов берутся
 * из макета (#CFC8C8 серые стрелки, #DE7879 акцент). Можно перекрыть пропами.
 */
import { ref, computed, watch } from 'vue'
import { useElementSize } from '@vueuse/core'

withDefaults(
  defineProps<{
    /** Прозрачность всего SVG. По умолчанию — 0.45 (утончённый вариант). */
    opacity?: number
    /** Цвет «стрелок-указателей» (8 серых glyph'ов в SVG). */
    arrowColor?: string
    /** Цвет «акцентных» линий и ромба (вертикаль/горизонталь/ромб). */
    accentColor?: string
  }>(),
  {
    opacity: 0.45,
    arrowColor: '#CFC8C8',
    accentColor: '#DE7879',
  },
)

/**
 * Высота rail в пикселях.
 *
 * Замеряется через `useElementSize` из @vueuse/core (ResizeObserver под
 * капотом, реактивно реагирует на любые изменения высоты секции).
 * Инициализация railH = 1000 — как у исходного Figma viewBox; при первом
 * же кадре после монтирования значение перетирается актуальной высотой,
 * благодаря чему `viewBox.height === container.height` и Y-scale = 1.0
 * (без сжатия/растяжения геометрии ромба).
 *
 * Минимум не выставляется намеренно: «Почему MTS» по макету ≈ 448px,
 * любая искусственная нижняя граница больше реальной высоты приводит
 * к Y-scale < 1 и сплющиванию ромба.
 */
const railEl = ref<SVGSVGElement | null>(null)
const { height } = useElementSize(railEl)
const railH = ref<number>(1000)

watch(height, (h) => {
  const rounded = Math.round(h)
  if (rounded > 0) {
    railH.value = rounded
  }
}, { immediate: true })

const viewBox = computed(() => `0 0 92 ${railH.value}`)
/** Сдвиг группы «низ + ромб»: исходно она была у y=833..1000, теперь — у нижнего края. */
const bottomShift = computed(() => railH.value - 1000)
/**
 * Координата нижнего конца центральной красной вертикали.
 *
 * В исходном SVG она шла до y=853.5 — на 146.5 пикселя выше нижнего края
 * (1000 − 853.5). Это почти у нижней вершины ромба (~y=855.6), благодаря
 * чему вертикаль пересекает горизонталь по центру ромба и образует
 * крест внутри ромба.
 */
const verticalEndY = computed(() => railH.value - 147)
</script>

<template>
  <svg
    ref="railEl"
    class="pointer-events-none absolute inset-y-0 right-0 z-[1] hidden h-full w-[92px] select-none lg:block"
    :style="{ opacity }"
    :viewBox="viewBox"
    preserveAspectRatio="none"
    fill="none"
    xmlns="http://www.w3.org/2000/svg"
    aria-hidden="true"
  >
    <!--
      Верхние «стрелки-указатели» (Figma Group 16/17 — y от 0 до ~284).
      Координаты те же, что в исходном экспорте, без масштабирования.
    -->
    <path
      d="M23.5 0.5L23.5 -2.18557e-08L22.5 2.18557e-08L22.5 0.5L23 0.5L23.5 0.5ZM23 144.387L25.8868 141.5L23 138.613L20.1133 141.5L23 144.387ZM23 0.5L22.5 0.5L22.5 141.5L23 141.5L23.5 141.5L23.5 0.5L23 0.5Z"
      :fill="arrowColor"
    />
    <path
      d="M23.5 213.5L23.5 213L22.5 213L22.5 213.5L23 213.5L23.5 213.5ZM23 287.387L25.8868 284.5L23 281.613L20.1133 284.5L23 287.387ZM23 213.5L22.5 213.5L22.5 284.5L23 284.5L23.5 284.5L23.5 213.5L23 213.5Z"
      :fill="arrowColor"
    />
    <path
      d="M23.5 284.5L23.5 284L22.5 284L22.5 284.5L23 284.5L23.5 284.5ZM23 357.387L25.8868 354.5L23 351.613L20.1133 354.5L23 357.387ZM23 284.5L22.5 284.5L22.5 354.5L23 354.5L23.5 354.5L23.5 284.5L23 284.5Z"
      :fill="arrowColor"
    />
    <path
      d="M23 216.387L25.8868 213.5L23 210.613L20.1133 213.5L23 216.387ZM23 141.5L22.5 141.5L22.5 213.5L23 213.5L23.5 213.5L23.5 141.5L23 141.5Z"
      :fill="arrowColor"
    />

    <!--
      Центральная красная вертикаль: единственный элемент, который
      растягивается по высоте секции. От низа верхней группы стрелок (y=354)
      до уровня ромба (y = H − 167).
    -->
    <line
      x1="23"
      y1="354"
      x2="23"
      :y2="verticalEndY"
      :stroke="accentColor"
      stroke-width="1"
      vector-effect="non-scaling-stroke"
    />

    <!--
      Нижняя группа: горизонталь + ромб + 2 стрелки-указателя.
      Сдвинута на (H − 1000), чтобы оставаться прижатой к нижнему краю
      и сохранить ту же геометрию (ромб = квадрат 31.4×31.4, повёрнутый −45°).
    -->
    <g :transform="`translate(0, ${bottomShift})`">
      <path
        d="M23.5 926.5C23.5 926.224 23.2761 926 23 926C22.7239 926 22.5 926.224 22.5 926.5L23 926.5L23.5 926.5ZM23 999.387L25.8868 996.5L23 993.613L20.1133 996.5L23 999.387ZM23 926.5L22.5 926.5L22.5 996.5L23 996.5L23.5 996.5L23.5 926.5L23 926.5Z"
        :fill="arrowColor"
      />
      <path
        d="M23.5 856.5C23.5 856.224 23.2761 856 23 856C22.7239 856 22.5 856.224 22.5 856.5L23 856.5L23.5 856.5ZM23 932.387L25.8868 929.5L23 926.613L20.1133 929.5L23 932.387ZM23 856.5L22.5 856.5L22.5 929.5L23 929.5L23.5 929.5L23.5 856.5L23 856.5Z"
        :fill="arrowColor"
      />
      <path d="M92 833.5H1" :stroke="accentColor" vector-effect="non-scaling-stroke" />
      <rect
        x="0.707107"
        y="833.429"
        width="31.4261"
        height="31.4261"
        transform="rotate(-45 0.707107 833.429)"
        :stroke="accentColor"
        vector-effect="non-scaling-stroke"
      />
    </g>
  </svg>
</template>
